package com.example.first4;

public class Exam {

    public String Name;
    public String Email;
    public String Phone;


    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public void setEmail(String email) {
        Email = email;
    }

    public void setPhone(String phone) {
        Phone = phone;
    }

    public String getEmail() {
        return Email;
    }

    public String getPhone() {
        return Phone;
    }

}

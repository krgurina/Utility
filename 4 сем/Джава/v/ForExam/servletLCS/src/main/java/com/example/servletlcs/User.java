package com.example.servletlcs;

public class User {

    public String Login = "";
    public String Code;

    public int Sum;

    public int getSum() {
        return Sum;
    }

    public void setSum(int sum) {
        Sum = sum;
    }

    public User() { }

    public String getLogin() {
        return Login;
    }

    public void setCode(String code) {
        Code = code;
    }

    public void setLogin(String login) {
        Login = login;
    }

    public String getCode() {
        return Code;
    }

}

